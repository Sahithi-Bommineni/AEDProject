public class HouseKeeping {
    
    int roomno;
    String roomcleaning;
    String restroom;
    String splins;
    String date;
    String time;
    String status;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getRoomno() {
        return roomno;
    }

    public void setRoomno(int roomno) {
        this.roomno = roomno;
    }

    public String getRoomcleaning() {
        return roomcleaning;
    }

    public void setRoomcleaning(String roomcleaning) {
        this.roomcleaning = roomcleaning;
    }

    public String getRestroom() {
        return restroom;
    }

    public void setRestroom(String restroom) {
        this.restroom = restroom;
    }

    public String getSplins() {
        return splins;
    }

    public void setSplins(String splins) {
        this.splins = splins;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
    
    
}
